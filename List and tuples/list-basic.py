my_list = [2,1,3,4]
# mutable and can store multiple data types
my_list.sort() # sorted in ascending order
my_list.sort(reverse=True) #sorted in descending order
my_list.reverse() #reverses the list
my_list.insert(2,5) # inserts element at index -> 5 at 2nd index
my_list.remove(1) # removes first occurence of element
my_list.pop(3) # removes element at index
my_list.append(6) #adds one element at the end